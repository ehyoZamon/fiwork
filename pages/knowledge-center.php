<script>
document.querySelectorAll(".faq-question").forEach(item => {
  item.addEventListener("click", () => {
    const active = document.querySelector(".faq-question.active");
    const icon = item.querySelector('.faq-icon');
    
    if (active && active !== item) {
      active.classList.remove("active");
      active.nextElementSibling.style.maxHeight = null;
      active.nextElementSibling.classList.remove("open");
      // Возвращаем плюс для предыдущей активной иконки
      const prevIcon = active.querySelector('.faq-icon');
      prevIcon.textContent = '+';
    }
    
    item.classList.toggle("active");
    const answer = item.nextElementSibling;
    
    if (item.classList.contains("active")) {
      answer.style.maxHeight = answer.scrollHeight + "px";
      answer.classList.add("open");
      // Меняем на минус
      icon.textContent = '−'; // Используем символ минуса, а не дефис
    } else {
      answer.style.maxHeight = null;
      answer.classList.remove("open");
      // Возвращаем плюс
      icon.textContent = '+';
    }
  });
});
</script>
